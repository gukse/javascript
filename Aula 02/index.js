
var numeroInserido = prompt("insira um numero entre 1 e 5")
if (numeroInserido == 1) {
    alert("Numero um")
}
else if (numeroInserido == 2) {
    alert("Numero dois")
}
else if (numeroInserido == 3) {
    alert("Numero tres")
}
else if (numeroInserido == 4) {
    alert("Numero quatro")
}
else if (numeroInserido == 5) {
    alert("Numero cinco")
}
else {
    alert ("Não é um numero entre 1 e 5")
}